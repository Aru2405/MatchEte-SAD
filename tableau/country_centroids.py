def enrich_country(country):
    """Devuelve dict con lat, lon, iso3, continent. None si no está mapeado."""
    if country not in COUNTRY_CENTROIDS:
        return {"lat": None, "lon": None, "iso3": None, "continent": None}
    lat, lon, iso3 = COUNTRY_CENTROIDS[country]
    return {
        "lat": lat,
        "lon": lon,
        "iso3": iso3,
        "continent": COUNTRY_TO_CONTINENT.get(country),
    }
